// var, let, const

// Var : Not used anymore, and it is globally scoped

// let: You can reassign value

/**Example */

let age  = 30;
age = 31

console.log(age);


// const: You can't change the value, and can't be directly reassigned


// Always use const, until and unless u want to reassign any value.

// In const, you can't initialise the value, u need to assign a value while in let, you can initialise without assigning a value.